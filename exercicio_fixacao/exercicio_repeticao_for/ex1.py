# Define o número máximo de valores a serem impressos
numero_maximo = 12

# Loop com for para imprimir os valores pares
for valor in range(2, numero_maximo + 1, 2):
    print(valor, end=" ")  # O "end=" " é usado para imprimir os valores na mesma linha com um espaço entre eles

print()  # Imprime uma linha em branco para finalizar a saída
